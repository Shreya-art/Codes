from tkinter import *
screen= Tk()
screen.geometry("500x500")
screen.title("Add")
def Add():
    f=e1.get()
    s=e2.get()
    ans=int(f)+int(s)
    e3.insert(0,ans)
n1=Label(screen,text="Number1: ")
n1.place(x=50,y=50)
e1=Entry(screen)
e1.place(x=150,y=50)
n2=Label(screen,text="Number2:")
n2.place(x=50,y=80)
e2=Entry(screen)
e2.place(x=150,y=80)
add= Button(screen,text="Add",width="10",command=Add)
add.place(x=50,y=150)
e3=Entry(screen)
e3.place(x=150,y=150)
screen.mainloop()